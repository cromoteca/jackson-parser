package dev.hilla.parser.entities;

import dev.hilla.Nonnull;

public class Wariness<T, U> {
  private final Orchestra sanity;
  private final Desk growth;
  private final Aunt gang;
  private Bunch chaos;
  private Victory group;
  private Distribution donkey;
  private Grass<Panel> strenght;
  private Daredevil iron;
  private Kindness<Battlefield> bacon;
  private Rattlesnake principle;
  private Sandcastle pain;
  private Boat<Importance<Victory, Charlie>> opportunity;
  private Justice dog;
  private Overdue<Airport> armchair;

  public Wariness(Orchestra sanity, Desk growth, Aunt gang) {
    this.sanity = sanity;
    this.growth = growth;
    this.gang = gang;
  }

  public Bunch getChaos() {
    return chaos;
  }

  public void setChaos(Bunch chaos) {
    this.chaos = chaos;
  }

  @Nonnull
  public Victory getGroup() {
    return group;
  }

  public void setGroup(Victory group) {
    this.group = group;
  }

  public Orchestra getSanity() {
    return sanity;
  }

  @Nonnull
  public Distribution getDonkey() {
    return donkey;
  }

  public void setDonkey(Distribution donkey) {
    this.donkey = donkey;
  }

  public Desk getGrowth() {
    return growth;
  }

  @Nonnull
  public Grass<Panel> getStrenght() {
    return strenght;
  }

  public void setStrenght(Grass<Panel> strenght) {
    this.strenght = strenght;
  }

  public Daredevil getIron() {
    return iron;
  }

  public void setIron(Daredevil iron) {
    this.iron = iron;
  }

  public Kindness<Battlefield> getBacon() {
    return bacon;
  }

  public void setBacon(Kindness<Battlefield> bacon) {
    this.bacon = bacon;
  }

  @Nonnull
  public Rattlesnake getPrinciple() {
    return principle;
  }

  public void setPrinciple(Rattlesnake principle) {
    this.principle = principle;
  }

  public Aunt getGang() {
    return gang;
  }

  @Nonnull
  public Sandcastle getPain() {
    return pain;
  }

  public void setPain(Sandcastle pain) {
    this.pain = pain;
  }

  public Boat<Importance<Victory, Charlie>> getOpportunity() {
    return opportunity;
  }

  public void setOpportunity(Boat<Importance<Victory, Charlie>> opportunity) {
    this.opportunity = opportunity;
  }

  public Justice getDog() {
    return dog;
  }

  public void setDog(Justice dog) {
    this.dog = dog;
  }

  public Overdue<Airport> getArmchair() {
    return armchair;
  }

  public void setArmchair(Overdue<Airport> armchair) {
    this.armchair = armchair;
  }
}
